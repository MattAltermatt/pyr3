// Generate src/flam3-palettes-data.ts from flam3's flam3-palettes.xml.
//
// flam3 ships a library of numbered palettes (flam3-palettes.xml) that the
// parser falls back to when a .flame carries no inline palette and instead
// references one by `<flame palette="N">` (parser.c:380, flam3_get_palette).
// pyr3 bakes that library into a committed data module so the fallback works
// in both consumers (browser + CLI) with zero runtime file I/O — PYR3-022.
//
// Encoding: each palette is 256 colors; flam3 stores them as `00RRGGBB` hex.
// We drop the leading alpha placeholder byte, pack the RGB triples for all
// palettes into one contiguous byte array, and base64-encode it (lossless,
// sync-decodable via atob in both environments, ~25% smaller than hex, and
// gzips well over the wire). Decoded once at first lookup in flam3-palettes.ts.
//
// Usage:  node scripts/gen-flam3-palettes.mjs <path/to/flam3-palettes.xml>
//         (or set FLAM3_PALETTES=<path>)

import { readFileSync, writeFileSync } from 'node:fs';

const xmlPath = process.argv[2] ?? process.env.FLAM3_PALETTES;
if (!xmlPath) {
  console.error(
    'usage: node scripts/gen-flam3-palettes.mjs <flam3-palettes.xml>  (or set FLAM3_PALETTES)',
  );
  process.exit(1);
}

const xml = readFileSync(xmlPath, 'utf8');

// <palette number="N" name="..." [smooth="..."] data="HEX with whitespace">
const re = /<palette\s+number="(\d+)"[^>]*\sdata="([0-9a-fA-F\s]+)"/g;
const byIndex = new Map();
let m;
while ((m = re.exec(xml)) !== null) {
  const idx = Number(m[1]);
  const hex = m[2].replace(/\s+/g, '');
  if (hex.length !== 256 * 8) {
    console.error(`palette ${idx}: expected 2048 hex chars (256×00RRGGBB), got ${hex.length}`);
    process.exit(1);
  }
  const rgb = new Uint8Array(256 * 3);
  for (let i = 0; i < 256; i++) {
    const base = i * 8; // skip byte 0 (the 00 alpha placeholder)
    rgb[i * 3 + 0] = parseInt(hex.slice(base + 2, base + 4), 16);
    rgb[i * 3 + 1] = parseInt(hex.slice(base + 4, base + 6), 16);
    rgb[i * 3 + 2] = parseInt(hex.slice(base + 6, base + 8), 16);
  }
  byIndex.set(idx, rgb);
}

if (byIndex.size === 0) {
  console.error('no <palette number=... data=...> entries matched — wrong file?');
  process.exit(1);
}

const count = Math.max(...byIndex.keys()) + 1;
for (let i = 0; i < count; i++) {
  if (!byIndex.has(i)) {
    console.error(`palette index ${i} missing — library is not contiguous 0..${count - 1}`);
    process.exit(1);
  }
}

const packed = new Uint8Array(count * 256 * 3);
for (let i = 0; i < count; i++) packed.set(byIndex.get(i), i * 256 * 3);
const b64 = Buffer.from(packed).toString('base64');

const out = `// AUTO-GENERATED by scripts/gen-flam3-palettes.mjs — DO NOT EDIT BY HAND.
//
// flam3's palette library (flam3-palettes.xml) — the canonical fallback when a
// .flame has no inline <palette>/<color>/<colors> block and references a
// library palette via <flame palette="N"> (PYR3-022). ${count} palettes ×
// 256 RGB triples, packed as contiguous bytes and base64-encoded (lossless).
// Decoded once at first lookup; see flam3-palettes.ts.

export const FLAM3_PALETTE_COUNT = ${count};

export const FLAM3_PALETTES_B64 =
  '${b64}';
`;

writeFileSync('src/flam3-palettes-data.ts', out);
console.log(
  `wrote src/flam3-palettes-data.ts: ${count} palettes, ${packed.length} bytes → ${b64.length} base64 chars`,
);
