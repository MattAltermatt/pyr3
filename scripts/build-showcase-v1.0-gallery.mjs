#!/usr/bin/env node
// Build .remember/verify/showcase-v1.0-gallery.html — 2-column gallery
// (kotlin v1.1 JPG ref / pyr3 BE 4K --preset 4k render) for all 55
// fixtures from kotlin's v1.0-showcase.txt. Run after
// `scripts/render-showcase-v1.0.mjs` populates fixtures/showcase-v1.0/.

import { readFileSync, existsSync, writeFileSync, mkdirSync, statSync } from 'node:fs';
import { resolve, basename, dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO = resolve(__dirname, '..');
const RENDERS_DIR = join(REPO, 'fixtures', 'showcase-v1.0');
const MANIFEST = join(RENDERS_DIR, '_manifest.json');
const KOTLIN_JPGS = '/Users/matt/dev/MattAltermatt/pyr3-kotlin/parity/showcase/out/v1.0.pages';
const OUT = join(REPO, '.remember', 'verify', 'showcase-v1.0-gallery.html');

if (!existsSync(MANIFEST)) {
  console.error(`manifest missing — run scripts/render-showcase-v1.0.mjs first: ${MANIFEST}`);
  process.exit(1);
}

const manifest = JSON.parse(readFileSync(MANIFEST, 'utf8'));
const fixtures = manifest.fixtures
  .map((fx) => {
    const renderPath = join(RENDERS_DIR, `${fx.id}.pyr3-4k.png`);
    const kotlinPath = join(KOTLIN_JPGS, `${fx.id}.gpu.4k.jpg`);
    return {
      id: fx.id,
      hasRender: existsSync(renderPath) && statSync(renderPath).size > 0,
      hasKotlin: existsSync(kotlinPath),
      renderPath, kotlinPath,
    };
  })
  .sort((a, b) => a.id.localeCompare(b.id));

const ready = fixtures.filter((f) => f.hasRender).length;
const total = fixtures.length;
const withKotlin = fixtures.filter((f) => f.hasKotlin).length;

const rows = fixtures
  .map(({ id, hasRender, hasKotlin, renderPath, kotlinPath }) => {
    const kotlinCell = hasKotlin
      ? `<figure><img src="file://${kotlinPath}" loading="lazy" /><figcaption>kotlin v1.1 4K (gpu.4k.jpg ref)</figcaption></figure>`
      : `<figure class="missing"><figcaption>kotlin JPG missing</figcaption></figure>`;
    const renderCell = hasRender
      ? `<figure><img src="file://${renderPath}" loading="lazy" /><figcaption>pyr3 BE 4K (--preset 4k)</figcaption></figure>`
      : `<figure class="missing"><figcaption>pyr3 render pending / failed</figcaption></figure>`;
    return `
<section class="fx">
  <h2><span class="fxname">${id}</span>${hasRender ? '' : ' <span class="tag-warn">render missing</span>'}</h2>
  <div class="grid">
    ${kotlinCell}
    ${renderCell}
  </div>
</section>`;
  })
  .join('\n');

const html = `<!doctype html>
<html lang="en">
<head>
<meta charset="utf-8" />
<title>pyr3 v1.0 showcase — kotlin v1.1 ref vs pyr3 BE 4K</title>
<style>
  body { background:#0e0e0e; color:#eee; font-family: ui-sans-serif, system-ui, sans-serif; margin: 0; padding: 24px 32px; }
  h1 { margin: 0 0 4px 0; font-weight: 600; }
  .subtitle { color: #aaa; margin-bottom: 18px; }
  .stats { font-family: ui-monospace, Menlo, monospace; font-size: 12px; color: #c4a850; background: #1a1610; border-left: 3px solid #5a4a14; padding: 8px 12px; margin-bottom: 24px; display: inline-block; }
  .fx { margin-bottom: 32px; padding-bottom: 24px; border-bottom: 1px solid #222; }
  .fx h2 { font-size: 15px; font-weight: 500; margin: 0 0 10px 0; display: flex; align-items: center; gap: 12px; }
  .fxname { font-family: ui-monospace, Menlo, monospace; color: #f0f0f0; }
  .tag-warn { font-family: ui-monospace, Menlo, monospace; font-size: 11px; padding: 2px 8px; border-radius: 4px; color: #f0d0a0; border: 1px solid #5a4a14; background: #2a2210; }
  .grid { display: grid; grid-template-columns: 1fr 1fr; gap: 10px; }
  figure { margin: 0; }
  figure img { width: 100%; display: block; border: 1px solid #2a2a2a; }
  figure.missing { aspect-ratio: 16 / 9; background: #1a1a1a; border: 1px dashed #3a3a3a; display: flex; align-items: center; justify-content: center; }
  figcaption { font-family: ui-monospace, Menlo, monospace; font-size: 11px; color: #999; padding-top: 5px; }
  figure.missing figcaption { color: #777; }
  .lede { background: #1a1a1a; border-left: 3px solid #4a7a4a; padding: 12px 16px; margin: 0 0 20px 0; max-width: 720px; }
  .lede p { margin: 4px 0; line-height: 1.5; }
</style>
</head>
<body>
<h1>pyr3 v1.0 showcase — kotlin v1.1 ref vs pyr3 BE 4K</h1>
<p class="subtitle">All 55 fixtures from kotlin's <code>v1.0-showcase.txt</code>, side-by-side.</p>

<div class="lede">
  <p>Generated by <code>scripts/render-showcase-v1.0.mjs</code> + <code>scripts/build-showcase-v1.0-gallery.mjs</code> on ${manifest.generatedAt.slice(0, 10)}. Pyr3 renders via <code>bin/pyr3-render.ts --preset 4k</code> (3840 long-edge, q=200, oversample=1; <code>src/presets.ts</code> mirrors kotlin's <code>Preset.SHOWCASE_4K</code>).</p>
  <p>Kotlin refs are pre-rendered JPGs from <code>pyr3-kotlin/parity/showcase/out/v1.0.pages/</code>. This is the visual story v1.0 ships — pyr3 reproducing kotlin v1.1's curated 55-flame showcase.</p>
</div>

<div class="stats">${ready}/${total} pyr3 renders present · ${withKotlin}/${total} kotlin refs available · render time ${manifest.totalSec}s (${manifest.rendered} rendered, ${manifest.skipped} skipped, ${manifest.failed} failed)</div>

${rows}

</body>
</html>
`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, html);
console.log(`wrote ${OUT}`);
console.log(`${ready}/${total} pyr3 renders · ${withKotlin}/${total} kotlin refs`);
